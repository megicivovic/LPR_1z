    private void loadFromFileCheckBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_loadFromFileCheckBoxItemStateChanged
        if (loadFromFileCheckBox.isSelected()) {
            fileField.setEnabled(true);
            delimiterComboBox.setEnabled(true);
            browseFileButton.setEnabled(true);
        } else {
            fileField.setEnabled(false);
            delimiterComboBox.setEnabled(false);
            browseFileButton.setEnabled(false);
        }
        
    }//GEN-LAST:event_loadFromFileCheckBoxItemStateChanged

    private void browseFileButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseFileButtonActionPerformed
        int option = this.fileChooser.showOpenDialog(this);

        if (option == JFileChooser.APPROVE_OPTION ) {
          File selectedFile = this.fileChooser.getSelectedFile();
          String  fileLocation = selectedFile.getPath();
          this.fileField.setText(fileLocation);
        }
    }//GEN-LAST:event_browseFileButtonActionPerformed


    private void jTextFieldTrainingKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldTrainingKeyReleased
        // TODO add your handling code here:
        try {
            int training = Integer.parseInt(getjTextFieldTraining().getText());
            getjLabel6().setText("");
            getjLabel6().setForeground(Color.red);
            if(training<0||training>100){
                getjLabel6().setText("Sum must be 100%");
                return;
            }
            String valueTestSet = String.valueOf(100 - training);
            getjTextFieldTest().setText(valueTestSet);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jTextFieldTrainingKeyReleased

